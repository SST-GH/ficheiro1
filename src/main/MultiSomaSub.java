package main;

public class MultiSomaSub {
	
	protected int x;
	protected int y;
	protected int z;
	
	public MultiSomaSub(int x, int y, int z) {
		x = x;
		y = y;
		z = z;
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}
}
	
		
		
		
	
	
	
